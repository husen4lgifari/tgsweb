
                function shareFacebook1(link) {
                  var encodedLink = encodeURIComponent(link);
                  var url = "https://www.facebook.com/sharer/sharer.php?u=" + encodedLink;
                  window.open(url, "_blank");
                  $('#shareModal1').modal('hide');
                }

                function shareTwitter1(link) {
                  var encodedLink = encodeURIComponent(link);
                  var url = "https://twitter.com/intent/tweet?url=" + encodedLink;
                  window.open(url, "_blank");
                  $('#shareModal1').modal('hide');
                }

                function shareEmail1(subject, body) {
                  var encodedSubject = encodeURIComponent(subject);
                  var encodedBody = encodeURIComponent(body);
                  var url = "mailto:?subject=" + encodedSubject + "&body=" + encodedBody;
                  window.location.href = url;
                  $('#shareModal1').modal('hide');
                }
